// Arduino Nano 接线：
//
// [FSR分压电路 — 检测鱼线张力]
//   5V ────[10kΩ电阻]──┬── A0（读模拟值 0-1023）
//                       └── FSR402 一端
//   FSR402 另一端 ──── GND
//   原理：拉力越大 → FSR阻值越小 → A0读值越大
//
// [震动马达驱动电路]
//   D9(PWM) ──[100Ω]── S8050三极管B极（基极）
//   S8050集电极 ────── 马达正极
//   S8050发射极 ────── GND
//   5V ──[1N4007二极管，正极接5V，负极接马达正极]── 马达正极
//   马达负极 ────────── GND
//   （1N4007作续流保护，防止马达断电时反向电动势损坏三极管）
//
// 采购清单（淘宝）：
