// ═══════════════════════════════════════════
// 鱼竿安装说明
// ═══════════════════════════════════════════
//
// 1. 震动马达（×2）：用扎带+热熔胶固定在鱼竿握把处
//    - 马达A：握把中部（主要震感）
//    - 马达B：握把顶部（远端震感，增加沉浸感，两个马达并联D9）
//
// 2. FSR传感器：内联在鱼线-弹力绳之间
//    竿稍 → 原装鱼线(5cm) → FSR402 → 弹力绳(25cm) → 地面锚点
//    FSR两根导线沿竿身走（透明胶布每10cm绑一道）→ 握把尾部穿出
//    → 1.5m延长线 → 展台内Arduino
//
// 3. 弹力绳固定：
//    展台前缘或地面打膨胀螺丝+挂钩，绑弹力绳末端
//    位置：观众脚前约80cm（介于观众与屏幕之间）
